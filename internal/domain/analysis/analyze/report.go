// WriteMarkdownReport generates a detailed Markdown report of the insights.
func WriteMarkdownReport(path string, insights []Insight) error {
	dir := filepath.Dir(path)
	if dir != "." {
		if err := os.MkdirAll(dir, 0700); err != nil {
			return err
		}
	}
	f, err := os.Create(path)
	if err != nil {
		return err
	}
	defer f.Close()

	highPriorityCount := 0
	for _, in := range insights {
		if in.Priority == "high" {
			highPriorityCount++
		}
	}

	filtered := filterInsightsForMarkdown(insights)
	top := topInsightsForMarkdown(filtered, 10)

	data := struct {
		Timestamp         string
		TotalHosts        int
		HighPriorityCount int
		Insights          []Insight
		TopInsights       []Insight
	}{
		Timestamp:         time.Now().Format(time.RFC1123),
		TotalHosts:        len(insights),
		HighPriorityCount: highPriorityCount,
		Insights:          filtered,
		TopInsights:       top,
	}

	funcMap := template.FuncMap{
		"join":          strings.Join,
		"reasonPreview": reasonPreview,
		"recommendedTools": func(tags, reasons, sources []string) []string {
			return recommendedTools(tags, reasons, sources)
		},
	}

	tmpl, err := template.New("report").Funcs(funcMap).Parse(markdownTemplate)
	if err != nil {
		return err
	}

	return tmpl.Execute(f, data)
}

func filterInsightsForMarkdown(insights []Insight) []Insight {
	filtered := make([]Insight, 0, len(insights))
	for _, in := range insights {
		// Hide low-information entries that are mostly seed/discovery without corroboration.
		if in.EvidenceCount == 0 {
			continue
		}
		if in.EvidenceCount < 2 && len(in.Sources) == 0 {
			continue
		}
		filtered = append(filtered, in)
	}
	return filtered
}

func reasonPreview(reasons []string) string {
	nonSource := make([]string, 0, len(reasons))
	sources := 0
	for _, r := range reasons {
		if strings.HasPrefix(r, "source: ") {
			sources++
			continue
		}
		nonSource = append(nonSource, r)
	}
	if len(nonSource) == 0 {
		if sources > 0 {
			return fmt.Sprintf("corroborated by %d tool signals", sources)
		}
		return "no strong behavioral signal collected yet"
	}
	if len(nonSource) > 2 {
		nonSource = nonSource[:2]
	}
	return strings.Join(nonSource, " | ")
}

func topInsightsForMarkdown(insights []Insight, n int) []Insight {
	if n <= 0 || len(insights) == 0 {
		return nil
	}
	cp := make([]Insight, len(insights))
	copy(cp, insights)
	sort.Slice(cp, func(i, j int) bool {
		if cp[i].Score != cp[j].Score {
			return cp[i].Score > cp[j].Score
		}
		return cp[i].Confidence > cp[j].Confidence
	})
	if len(cp) > n {
		cp = cp[:n]
	}
	return cp
}

// WriteCSVSummary generates a CSV summary of the insights.
func WriteCSVSummary(path string, insights []Insight) error {
	dir := filepath.Dir(path)
	if dir != "." {
		if err := os.MkdirAll(dir, 0700); err != nil {
			return err
		}
	}
	f, err := os.Create(path)
	if err != nil {
		return err
	}
	defer f.Close()

	writer := csv.NewWriter(f)

	if err := writer.Write([]string{"host", "dedupe_key", "severity", "score", "confidence", "sources", "tags", "reasons", "recommended_tools", "suggested_tests", "evidence_count"}); err != nil {
		return err
	}
	for _, in := range insights {
		dk := in.DedupeKey
		if dk == "" {
			dk = in.Host
		}
		if err := writer.Write([]string{
			in.Host,
			dk,
			in.Priority,
			fmt.Sprintf("%d", in.Score),
			fmt.Sprintf("%d", in.Confidence),
			strings.Join(in.Sources, "; "),
			strings.Join(in.Tags, "; "),
			strings.Join(in.Reasons, "; "),
			strings.Join(recommendedTools(in.Tags, in.Reasons, in.Sources), "; "),
			strings.Join(in.SuggestedTests, "; "),
			fmt.Sprintf("%d", in.EvidenceCount),
		}); err != nil {
			return err
		}
	}

	writer.Flush()
	if err := writer.Error(); err != nil {
		return err
	}
	return nil
}

// WriteHackerOneCSV generates a CSV formatted for HackerOne bulk import.
