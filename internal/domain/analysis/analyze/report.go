// ExportToObsidian creates individual notes in an Obsidian vault for each high-priority host.
func ExportToObsidian(dir string, insights []Insight) error {
	if err := os.MkdirAll(dir, 0700); err != nil {
		return err
	}

	for _, in := range insights {
		if in.Priority != "high" && in.Score < 25 {
			continue
		}

		filename := filepath.Join(dir, in.Host+".md")
		f, err := os.Create(filename)
		if err != nil {
			continue
		}

		fmt.Fprintf(f, "---\n")
		fmt.Fprintf(f, "tags: [bbpts, %s]\n", strings.Join(in.Tags, ", "))
		fmt.Fprintf(f, "priority: %s\n", in.Priority)
		fmt.Fprintf(f, "score: %d\n", in.Score)
		fmt.Fprintf(f, "updated: %s\n", time.Now().Format("2006-01-02"))
		fmt.Fprintf(f, "---\n\n")
		fmt.Fprintf(f, "# %s\n\n", in.Host)
		fmt.Fprintf(f, "## Findings\n")
		for _, r := range in.Reasons {
			fmt.Fprintf(f, "- %s\n", r)
		}
		fmt.Fprintf(f, "\n## Suggested Tests\n")
		for _, t := range in.SuggestedTests {
			fmt.Fprintf(f, "- [ ] %s\n", t)
		}
		fmt.Fprintf(f, "\n## Recommended Tools\n")
		for _, tool := range recommendedTools(in.Tags, in.Reasons, in.Sources) {
			fmt.Fprintf(f, "- %s\n", tool)
		}

		f.Close()
	}

	return nil
}

func recommendedTools(tags, reasons, sources []string) []string {
	set := make(map[string]struct{})
	add := func(values ...string) {
		for _, v := range values {
			v = strings.TrimSpace(v)
			if v != "" {
				set[v] = struct{}{}
			}
		}
	}

	// Core triage stack when there is enough evidence to investigate.
	add("httpx", "nuclei")

	tagSet := make(map[string]struct{}, len(tags))
	for _, t := range tags {
		tagSet[strings.ToLower(strings.TrimSpace(t))] = struct{}{}
	}

	if _, ok := tagSet["parameterized"]; ok {
		add("ffuf")
	}
	if _, ok := tagSet["sqli-candidate"]; ok {
		add("sqlmap", "ghauri")
	}
	if _, ok := tagSet["ssrf-candidate"]; ok {
		add("interactsh-client", "ffuf")
	}
	if _, ok := tagSet["lfi-candidate"]; ok {
		add("ffuf", "wfuzz")
	}
	if _, ok := tagSet["auth"]; ok {
		add("burpsuite", "caido")
	}
	if _, ok := tagSet["api"]; ok {
		add("kiterunner", "ffuf")
	}
	if _, ok := tagSet["interesting-js"]; ok {
		add("katana", "linkfinder", "secretfinder")
	}
	if _, ok := tagSet["cors-risk"]; ok {
		add("corsy")
	}
	if _, ok := tagSet["manual-bypass"]; ok {
		add("burpsuite", "curl")
	}
	if _, ok := tagSet["sensitive"]; ok {
		add("ripgrep", "nuclei")
	}

	for _, r := range reasons {
		rLower := strings.ToLower(r)
		switch {
		case strings.Contains(rLower, "source: naabu"):
			add("naabu", "nmap")
		case strings.Contains(rLower, "source: gau"), strings.Contains(rLower, "source: katana"), strings.Contains(rLower, "source: hakrawler"):
			add("katana", "gau")
		case strings.Contains(rLower, "source: subfinder"), strings.Contains(rLower, "source: crtsh"):
			add("subfinder", "dnsx")
		case strings.Contains(rLower, "source: httpx"):
			add("httpx")
		}
	}

	for _, s := range sources {
		switch strings.ToLower(strings.TrimSpace(s)) {
		case "naabu":
			add("naabu", "nmap")
		case "httpx":
			add("httpx")
		case "subfinder", "crtsh":
			add("subfinder", "dnsx")
		case "gau", "katana", "hakrawler":
			add("katana", "gau")
		}
	}

	out := make([]string, 0, len(set))
	for tool := range set {
		out = append(out, tool)
	}
	sort.Strings(out)
	return out
}
